// -----------------------------------------------------------------------------
// Converter generation against REAL OpenSearch.Client wire formats (issue #388).
//
// Emits a single polymorphic System.Text.Json converter for a family of leaf
// queries, from a declarative spec describing each variant's wire SHAPE. The
// shapes were derived from the real client's output:
//   Empty       : {"<variant>":{}}                              (match_all)
//   FieldOnly   : {"<variant>":{"field":"<field>"}}             (exists)
//   FieldValue  : {"<variant>":{"<field>":{"<valueKey>":<v>}}}  (term/prefix/wildcard/regexp/match)
//
// In production this spec comes from the OpenSearch OpenAPI model via
// src/ApiGenerator; here it is hand-fed to prove the generated converter
// reproduces the real wire format byte-for-byte (validated by parity tests).
// -----------------------------------------------------------------------------

using System;
using System.Collections.Generic;
using System.IO;
using System.Text;

namespace StjConverterGen;

internal enum Shape { Empty, FieldOnly, FieldValue, Compound }

internal sealed record VariantSpec(string Json, string ClrType, Shape Shape, string ValueKey = "value");

internal static class Program
{
    private static int Main(string[] args)
    {
        var outDir = args.Length > 0 ? args[0] : ".";
        Directory.CreateDirectory(outDir);

        const string ns = "OpenSearch.Net.Stj.Tests.Model";
        const string baseType = "RealLeafQuery";
        const string converterName = "GeneratedRealLeafQueryConverter";

        var variants = new[]
        {
            new VariantSpec("match_all", "MatchAllLeaf", Shape.Empty),
            new VariantSpec("exists", "ExistsLeaf", Shape.FieldOnly),
            new VariantSpec("term", "TermLeaf", Shape.FieldValue),
            new VariantSpec("prefix", "PrefixLeaf", Shape.FieldValue),
            new VariantSpec("wildcard", "WildcardLeaf", Shape.FieldValue),
            new VariantSpec("regexp", "RegexpLeaf", Shape.FieldValue),
            new VariantSpec("match", "MatchLeaf", Shape.FieldValue, ValueKey: "query"),
            new VariantSpec("bool", "BoolLeaf", Shape.Compound),
        };

        EmitToFile(outDir, ns, baseType, converterName, variants);

        // Second polymorphic family: aggregations (reuse the FieldOnly template).
        var aggVariants = new[]
        {
            new VariantSpec("terms", "TermsAgg", Shape.FieldOnly),
            new VariantSpec("max", "MaxAgg", Shape.FieldOnly),
        };
        EmitToFile(outDir, ns, "RealAggregation", "GeneratedRealAggregationConverter", aggVariants);

        return 0;
    }

    private static void EmitToFile(string outDir, string ns, string baseType, string converterName, IReadOnlyList<VariantSpec> variants)
    {
        var src = Emit(ns, baseType, converterName, variants);
        var path = Path.Combine(outDir, converterName + ".cs");
        File.WriteAllText(path, src);
        Console.WriteLine($"Generated converter '{converterName}' for {variants.Count} variant(s) -> {path}");
    }

    private static string Emit(string ns, string baseType, string converterName, IReadOnlyList<VariantSpec> variants)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated> Emitted by StjConverterGen (PoC for #388). Do not edit.");
        sb.AppendLine("#nullable enable");
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Text.Json;");
        sb.AppendLine("using System.Text.Json.Serialization;");
        sb.AppendLine();
        sb.AppendLine($"namespace {ns};");
        sb.AppendLine();
        sb.AppendLine($"public sealed class {converterName} : JsonConverter<{baseType}>");
        sb.AppendLine("{");
        sb.AppendLine("    private readonly Func<string, string> _fieldInferrer;");
        sb.AppendLine($"    public {converterName}(Func<string, string>? fieldInferrer = null) => _fieldInferrer = fieldInferrer ?? (s => s);");
        sb.AppendLine();
        sb.AppendLine($"    public override void Write(Utf8JsonWriter writer, {baseType} value, JsonSerializerOptions options)");
        sb.AppendLine("    {");
        sb.AppendLine("        writer.WriteStartObject();");
        sb.AppendLine("        writer.WritePropertyName(value.Variant);");
        sb.AppendLine("        switch (value)");
        sb.AppendLine("        {");
        foreach (var v in variants)
        {
            sb.AppendLine($"            case {v.ClrType} q:");
            switch (v.Shape)
            {
                case Shape.Empty:
                    sb.AppendLine("                writer.WriteStartObject();");
                    sb.AppendLine("                writer.WriteEndObject();");
                    break;
                case Shape.FieldOnly:
                    sb.AppendLine("                writer.WriteStartObject();");
                    sb.AppendLine("                writer.WriteString(\"field\", _fieldInferrer(q.Field));");
                    sb.AppendLine("                writer.WriteEndObject();");
                    break;
                case Shape.FieldValue:
                    sb.AppendLine("                writer.WriteStartObject();");
                    sb.AppendLine("                writer.WritePropertyName(_fieldInferrer(q.Field));");
                    sb.AppendLine("                writer.WriteStartObject();");
                    sb.AppendLine($"                writer.WriteString(\"{v.ValueKey}\", q.Value);");
                    sb.AppendLine("                writer.WriteEndObject();");
                    sb.AppendLine("                writer.WriteEndObject();");
                    break;
                case Shape.Compound:
                    sb.AppendLine("                writer.WriteStartObject();");
                    sb.AppendLine("                writer.WritePropertyName(\"must\");");
                    sb.AppendLine("                writer.WriteStartArray();");
                    sb.AppendLine("                foreach (var item in q.Must)");
                    sb.AppendLine("                    JsonSerializer.Serialize(writer, item, options); // recursion");
                    sb.AppendLine("                writer.WriteEndArray();");
                    sb.AppendLine("                writer.WriteEndObject();");
                    break;
            }
            sb.AppendLine("                break;");
        }
        sb.AppendLine("            default:");
        sb.AppendLine($"                throw new JsonException($\"Unsupported {baseType} '{{value.Variant}}'\");");
        sb.AppendLine("        }");
        sb.AppendLine("        writer.WriteEndObject();");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine($"    public override {baseType} Read(ref Utf8JsonReader reader, Type typeToConvert, JsonSerializerOptions options)");
        sb.AppendLine("    {");
        sb.AppendLine("        if (reader.TokenType != JsonTokenType.StartObject) throw new JsonException();");
        sb.AppendLine("        reader.Read(); // variant property");
        sb.AppendLine("        var variant = reader.GetString();");
        sb.AppendLine("        reader.Read(); // body start");
        sb.AppendLine($"        {baseType} result;");
        sb.AppendLine("        switch (variant)");
        sb.AppendLine("        {");
        foreach (var v in variants)
        {
            sb.AppendLine($"            case \"{v.Json}\":");
            sb.AppendLine("            {");
            switch (v.Shape)
            {
                case Shape.Empty:
                    sb.AppendLine("                reader.Skip(); // body EndObject");
                    sb.AppendLine($"                result = new {v.ClrType}();");
                    break;
                case Shape.FieldOnly:
                    sb.AppendLine("                reader.Read(); // \"field\" property");
                    sb.AppendLine("                reader.Read(); // field value");
                    sb.AppendLine("                var f = reader.GetString() ?? \"\";");
                    sb.AppendLine("                reader.Read(); // body EndObject");
                    sb.AppendLine($"                result = new {v.ClrType} {{ Field = f }};");
                    break;
                case Shape.FieldValue:
                    sb.AppendLine("                reader.Read(); // field property name");
                    sb.AppendLine("                var f = reader.GetString() ?? \"\";");
                    sb.AppendLine("                reader.Read(); // inner StartObject");
                    sb.AppendLine("                reader.Read(); // value-key property");
                    sb.AppendLine("                reader.Read(); // value");
                    sb.AppendLine("                var val = reader.GetString() ?? \"\";");
                    sb.AppendLine("                reader.Read(); // inner EndObject");
                    sb.AppendLine("                reader.Read(); // body EndObject");
                    sb.AppendLine($"                result = new {v.ClrType} {{ Field = f, Value = val }};");
                    break;
                case Shape.Compound:
                    sb.AppendLine("                throw new NotSupportedException(\"Compound read not generated in this PoC.\");");
                    break;
            }
            if (v.Shape != Shape.Compound) sb.AppendLine("                break;");
            sb.AppendLine("            }");
        }
        sb.AppendLine($"            default: throw new JsonException($\"Unsupported {baseType} '{{variant}}'\");");
        sb.AppendLine("        }");
        sb.AppendLine("        reader.Read(); // wrapper EndObject");
        sb.AppendLine("        return result;");
        sb.AppendLine("    }");
        sb.AppendLine("}");
        return sb.ToString();
    }
}
